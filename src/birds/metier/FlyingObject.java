package birds.metier;

import birds.gui.Sky;

public abstract class FlyingObject {
	int x, y, size;
	Sky sky;
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public int getSize() {
		return size;
	}
	
	public void setX(int x) {
		this.x = x;
	}
	
	public void setY(int y) {
		this.y = y;
	}
	
	
	public abstract void fly();
}
